$(document).ready(function () {
    // When the document loads, put all our common passwords into a trie
    var common_password_tree = buildPasswordTree();

    // Check password strength whenever the user releases a key on either input
    $('#password').keyup(function () {
        $('#message').html(checkPasswordStrength($('#password').val()))
    })

    $('#username').keyup(function () {
        $('#message').html(checkPasswordStrength($('#password').val()))
    })

    function checkPasswordStrength(password) {
        // How many tips the user has followed
        var user_score = 0
        $('#score').html(user_score)
        $('.score-container').removeClass('max-score')
        $('.todo-list > div').removeClass()
        $('.todo-list > div').addClass('unchecked')

        var strength = 0
        // NIST Password Length Guidelines: Password >= 8 characters and <= 64 characters
        if (password.length < 8) {
            $('#message').removeClass()
            $('#message').addClass('short')
            return 'Your password is too short, try adding more to it'
        } else {
            $('.todo-list > div:eq(0)').removeClass()
            $('.todo-list > div:eq(0)').addClass('checked')
            user_score++;
        }
        if (password.length > 32) {
            $('#message').removeClass()
            $('#message').addClass('long')
            return 'Your password is too long to remember'
        }

        // Check username similarity if a username has been entered
        var username = $('#username').val()
        if (username.length > 0){
            var username_similarity = lCS(username,password) * 1.0
            if (username_similarity / password.length > 0.5){
                $('#message').removeClass()
                $('#message').addClass('weak')
                return 'Your password is very similar to your username'
            }
        }

        // Check if the password is one from our list of common ones
        if (common_password_tree.contains(password)) {
            $('#message').removeClass()
            $('#message').addClass('weak')
            return 'This is a very common password :('
        }

        // Translate any numbers and check against common passwords
        // NOTE: only run this for short passwords to avoid slowdown
        if (password.length < 15) {
            for (translation of fromLeet(password)) {
                if (common_password_tree.contains(translation)) {
                    $('#message').removeClass()
                    $('#message').addClass('weak')
                    return `Replacing letters with numbers isn't enough ;)`
                }
            }
        }

        // Increase password strength by 1 if password is long enough
        if (password.length > 7) strength += 1
        // Increase password strength by 1 if password contains both lower and uppercase characters
        if (password.match(/([a-z].*[A-Z])|([A-Z].*[a-z])/)) {
            strength += 1
            $('.todo-list > div:eq(1)').removeClass()
            $('.todo-list > div:eq(1)').addClass('checked')
            user_score++;
        }
        // Increase password strength by 1 if password contains both numbers and characters
        if (password.match(/([a-zA-Z])/) && password.match(/([0-9])/)) {
            strength += 1
            $('.todo-list > div:eq(2)').removeClass()
            $('.todo-list > div:eq(2)').addClass('checked')
            user_score++;
        }
        // Increase password strength by 1 if password has at least one special character
        if (password.match(/([^\w\s])/)) {
            strength += 1
            $('.todo-list > div:eq(3)').removeClass()
            $('.todo-list > div:eq(3)').addClass('checked')
            user_score++;
        }

        $('#score').html(user_score)
        if (user_score == 4) {
            $('.score-container').addClass('max-score')
        }

        // Increase password strength by 1 if password has at least two special characters
        if (password.match(/(.*[^\w\s].*[^\w\s])/)) {
            strength += 1
        }
        // After calculating the password strength, we can return appropriate messages
        if (strength < 2) {
            $('#message').removeClass()
            $('#message').addClass('weak')
            return 'Your password is very weak, look at the tips below'
        } else if (strength == 2) {
            $('#message').removeClass()
            $('#message').addClass('good')
            return 'Your password is getting better, keep going!'
        } else {
            $('#message').removeClass()
            $('#message').addClass('strong')
            return `That's a strong password! Well done!`
        }
    }
});


// Compute the longest common subsequence between two strings, can be used to check username/password similarity
function lCS(X, Y){
    C = Array(X.length)
    for (var i = 0; i < X.length;i++){
        C[i] = Array(Y.length)
        for (var j = 0; j < Y.length;j++){
            C[i][j] = 0
        }
    }
    for (var i = 1; i < X.length;i++){
        for (var j = 1; j < Y.length;j++){
            if (X[i] == Y[j]){
                C[i][j] = C[i-1][j-1] + 1
            }
            else {
                C[i][j] = Math.max(C[i][j-1],C[i-1][j])
            }
        }
    }
    return C[X.length-1][Y.length-1]
}
    

// Gather all the possible 1337 translations of a word
// NOTE: this is combinatorial in complexity, so don't run it for very long passwords
function fromLeet(string) {
    combinations = []
    for (char of string) {
        // Gather possible translations of the character
        translations = []
        if (char in leet) {
            for (possible of leet[char]) {
                translations.push(possible)
            }
        } else {
            translations.push(char)
        }
        // Append each possible combination
        if (combinations.length == 0) {
            for (trans of translations) {
                combinations.push(trans)
            }
        } else {
            new_combos = []
            for (combo of combinations) {
                for (trans of translations) {
                    new_combos.push(combo + trans)
                }
            }
            combinations = new_combos
        }
    }
    return combinations
}

// A dictionary to convert from numbers to letters 
var leet = {
    '0': ['o'],
    '1': ['l', 'i'],
    '2': ['z'],
    '3': ['e'],
    '4': ['a'],
    '5': ['s'],
    '6': ['g'],
    '7': ['t'],
    '8': ['b'],
    '9': ['g'],
}


// Class to be used in our trie data structure. Each Node is assumed to contain one character or the string "end"
class TreeNode {
    constructor(c) {
        this.contents = c;
        this.children = new Array();
    }
    addChild(child) {
        this.children.push(child);
    }
    contains(sequence) {
        var current = this;
        for (let index = 0; index < sequence.length; index++) {
            character = sequence[index];

            var next = null;
            current.children.forEach(child => {
                if (child.contents == character) next = child;
            });

            if (next == null) return false;
            current = next;
        }
        return current.children.map(node => node.contents).includes("end");
    }
}

function buildPasswordTree() {
    pass_array = common_passwords.split('\n');
    var common_password_tree = new TreeNode('');
    // Add each password to our tree
    pass_array.forEach(element => {
        // Reset our current pointer to the root of the tree
        var current = common_password_tree;
        for (let index = 0; index < element.length; index++) {
            character = element[index];
            // If current has no children, add our letter as a child
            if (current.children.length == 0) {
                new_child = new TreeNode(character);
                current.addChild(new_child);
                current = new_child;
                continue;
            }

            var next = null;
            current.children.forEach(child => {
                if (child.contents == character) next = child;
            });
            // If current has children but none of them are our letter, add our letter as a child
            if (next == null) {
                new_child = new TreeNode(character);
                current.addChild(new_child);
                current = new_child;
                continue;
            }
            // If current has our letter as a child, move onto that one
            current = next;
        }
        // Append a terminating character
        current.addChild(new TreeNode('end'));
    });
    return common_password_tree;
}

